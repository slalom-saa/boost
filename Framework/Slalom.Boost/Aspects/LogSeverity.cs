namespace Slalom.Boost.Aspects
{
    /// <summary>
    /// Indicates the log severity.
    /// </summary>
    public enum LogSeverity
    {
        /// <summary>
        /// Indicates no severity has been set.
        /// </summary>
        None,

        /// <summary>
        /// Indicates a debug severity.
        /// </summary>
        Debug,

        /// <summary>
        /// Indicates an information severity.
        /// </summary>
        Information,

        /// <summary>
        /// Indicates a warning severity.
        /// </summary>
        Warning,

        /// <summary>
        /// Indicates an error severity.
        /// </summary>
        Error
    }
}